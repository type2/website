<b><font size="2" face="Arial, Helvetica, sans-serif">Special paint codes</font></b><br>
<br>
<font size="2" face="Arial, Helvetica, sans-serif">If a car got a special paint job in the factory a special paint instruction 
  was applied. In German this is called a '<i><b>Sonderlackierung</b></i>'. During modelyears 
  <b>1968-1971</b> the code starting with a '<b>5</b>'. From <b>1972</b> onwards the code started with a '<b>9</b>'.
  A Sonderlackierung was needed when a paint job was applied that was different from the regular program. Examples are: special company colours,
  certain body parts painted in other colours or VW-colours that were normally not available for a specific model or for that model year.
  Factory produced Westfalia campmobiles all came with a Sonderlackierung. These vehicles are all based on a VW Kombi. Kombi's had a
  grey or beige painted cargo compartment but Westfalia required the rear compart to be painted in the body colour. Hence the special
  paint job instruction.</font></p>
<font size="2" face="Arial, Helvetica, sans-serif"><p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong>Sonderlackierung 
  (special paint)</strong></font></p>
<p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Busses produced 
  for a special purpose often don't have the standard paint color codes. They 
  have a 'Sonderlackierung', which means they have a special paint color. Examples 
  of such vehicles are: Westfalia campmobiles, Army vehicles, Fire Trucks and 
  German mail vehicles.They have a code starting with a '5' or a '9' in most cases. 
  For example: '9135' stands for 'postgelb' ('Mail Yellow') a color used exclusively 
  for the 'Deutsche Bundespost' (German Mail).<br>
  It's not clear why VW used special paint codes for Westfalia campmobiles. The 
  colors on these busses are actually standardcolors, but are indicated by a special 
  code.</font></p>
</font>
