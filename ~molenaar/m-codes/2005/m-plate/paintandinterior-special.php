<font size="3" face="Arial, Helvetica, sans-serif"><b>Colour codes for 1972-1979 models</b></font><p>
<font size="2" face="Arial, Helvetica, sans-serif">
At the start of model year 1972 the Sonderlackierung code was changed. From this year onwards they are distinguised from the regular paint
codes by the first number, a '9'. The three folowing digits identify the Sonderlackierung. The difference with the old 6-digit
sonderlackierung code is that there is space left for the interior code.
</font><p>

