<p>
<b><font size="2" face="Arial, Helvetica, sans-serif">1968-1971 Models</font></b><br>
<font size="2" face="Arial, Helvetica, sans-serif">During model year 1968-1971 a 6-digit code was used. This left no space for
an interior code. Therefore the buses with a Sonderlackierung got a standard interior that was used in that year. This is 'beige'
for 1968 and 'dark beige' for 1969-1971. By using and M-code it was possible to get a different interior colour.</font><br>
</p>


